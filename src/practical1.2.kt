fun main(){

        var intVal:Int = 10
        println("Integer Value : ${intVal}")
        println("Double Value (From Integer): ${intVal.toDouble()}")

        var stringVal:String = "10"
        println("String Value : ${stringVal}")
        println("Integer Value1 (From String) : ${stringVal.toInt()}")
        println("Integer Value2 (From String) : ${stringVal.toInt()}")

        var stringVal2:String = "10.12"
        println("Double Value (From String) : ${stringVal2.toDouble()}")

}